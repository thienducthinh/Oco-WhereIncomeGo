class User():
    def __init__(self, first_name, last_name, username, address, zipcode, city, state, country, pin, password=None, email=None, phone_number=None):
        self.first_name = first_name
        self.last_name = last_name
        self.username = username
        self.password = password
        self.email = email
        self.address = address
        self.zipcode = zipcode
        self.city = city
        self.state = state
        self.country = country
        self.pin = pin
        self.phone_number = phone_number







